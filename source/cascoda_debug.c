/******************************************************************************/
/******************************************************************************/
/****** dps()                                                            ******/
/******************************************************************************/
/****** Brief:  DP Character String                                      ******/
/******************************************************************************/
/****** Param:  pString - Pointer to String                              ******/
/******************************************************************************/
/****** Return: -                                                        ******/
/******************************************************************************/
/******************************************************************************/
void dps(uint8_t *pString, void *pDeviceRef)
{
	while (*pString != 0) {
		Putc(*pString++, pDeviceRef);
	}
} // End of dps()


