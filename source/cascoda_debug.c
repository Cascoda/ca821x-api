/******************************************************************************/
/******************************************************************************/
/****** dpDec()                                                          ******/
/******************************************************************************/
/****** Brief:  DP 16-Bit Unsigned Decimal                               ******/
/******************************************************************************/
/****** Param:  -                                                        ******/
/******************************************************************************/
/****** Return: -                                                        ******/
/******************************************************************************/
/******************************************************************************/
void dpDec(uint16_t Out, void *pDeviceRef)
{
	uint8_t Sig = 0;
	uint16_t Div, Num, Rem;

	Num = Out;
	for (Div = 10000; Div > 1; Div /= 10) {
		Rem = Num%Div;
		Num = Num/Div;
		if (Num) {
			Sig = 1;
			Putc(HexChars[Num], pDeviceRef);
		} else if (Sig) {
			Putc('0', pDeviceRef);
		}
		Num = Rem;
	}
	Putc(HexChars[Num], pDeviceRef);
} // End of dpDec()


