/******************************************************************************/
/******************************************************************************/
/****** dp_u8_3u()                                                       ******/
/******************************************************************************/
/****** Brief:  DP uint8_t to 3-Digit Unsigned                              ******/
/******************************************************************************/
/****** Param:  d - Output Value                                         ******/
/******************************************************************************/
/****** Return: -                                                        ******/
/******************************************************************************/
/******************************************************************************/
void dp_u8_3u(uint8_t d, void *pDeviceRef)
{
	uint8_t i, num, div, rem, ndone;

	num = d;
	ndone = 0;
	div=100;
	for (i = 0; i < 2; ++i) {
		rem = num%div;
		num = num/div;
		if (num) {
			ndone = 1;
			Putc(HexChars[num], pDeviceRef);
		} else {
			if (ndone)
				Putc('0', pDeviceRef);
			else
				Putc(' ', pDeviceRef);
		}
		num = rem;
		div /= 10;
	}
	Putc(HexChars[num], pDeviceRef);
} // End of dp_u8_3u()


