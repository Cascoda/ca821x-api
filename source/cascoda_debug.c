/******************************************************************************/
/******************************************************************************/
/****** dparray()                                                        ******/
/******************************************************************************/
/****** Brief:  DP Array of Hexadecimal 8-bit Unsigned                   ******/
/******************************************************************************/
/****** Param:  pArray - Pointer to Array                                ******/
/****** Param:  Length - Length of Array                                 ******/
/******************************************************************************/
/****** Return: -                                                        ******/
/******************************************************************************/
/******************************************************************************/
void dparray(uint8_t *pArray, uint8_t Length, void *pDeviceRef)
{
	while (Length) {
		dp8(*pArray++, pDeviceRef);
		Length--;
	}
} // End of dparray()


/******************************************************************************/
/******************************************************************************/
/****** dparrayle()                                                      ******/
/******************************************************************************/
/****** Brief:  DP Inverse Array of Hexadecimal 8-bit Unsigned           ******/
/******************************************************************************/
/****** Param:  pArray - Pointer to Array                                ******/
/****** Param:  Length - Length of Array                                 ******/
/******************************************************************************/
/****** Return: -                                                        ******/
/******************************************************************************/
/******************************************************************************/
void dparrayle(uint8_t *pArray, uint8_t Length, void *pDeviceRef)
{
	while (Length) {
		Length--;
		dp8(pArray[Length], pDeviceRef);
	}
} // End of dparrayle()


